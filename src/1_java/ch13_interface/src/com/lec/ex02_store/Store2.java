package com.lec.ex02_store;

public class Store2 implements HeadQuarterStore {
	private String na;

	public Store2(String na) {
		this.na = na;
	}

	// 오버라이드(재정의) : 부모클래스의 메소드를 재정의
	// 오버로드(중복정의) : 매개변수의 수나 타입으로 같은 이름의 함수를 중복 정의
	@Override
	public void kj() {
		System.out.println("김치찌개 5,000원");

	}

	@Override
	public void bj() {
		System.out.println("부대찌개 5,000원");
	}

	@Override
	public void bib() {
		System.out.println("비빔밥 5,000원");
	}

	@Override
	public void sd() {
		System.out.println("순대국 5,000d원");

	}

	@Override
	public void gob() {
		System.out.println("공기밥 무료");
	}

	public String getNa() {
		return na;
	}

}
